#include <ft_ls.h>

/*
** Return 0 if strcmp($s, ".") == 0 || strcmp($s, "..") == 0
** otherwise return 1
*/

int		ft_can_recurse(char *s)
{
	if (s && s[0] == '.')
	{
		if (!s[1] || ft_strcmp(s, "..") == 0)
			return (0);
	}
	return (1);
}