#include <ft_ls.h>

int		ft_ls_str_alphacmp(char *s1, char *s2)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (s1[i] == '.')
		i++;
	while (s2[j] == '.')
		j++;
	while (s1[i] == s2[j] || (s1[i] - 32) == s2[j] || (s2[j] - 32) == s1[i])
	{
		i++;
		j++;
	}
	return (s1[i] - s2[j]);
}