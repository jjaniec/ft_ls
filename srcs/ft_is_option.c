#include <ft_ls.h>

int		ft_is_option(char *str)
{
	if (*str != 'l' && *str != 'R' && \
		*str != 'a' && *str != 'r' && \
		*str != 't' && *str != 'n' && \
		*str != 'G' && *str != 'A')
		return (0);
	return (1);
}
