#include <ft_ls.h>

int		ft_is_option(char *str)
{
	if (*str == '-' || \
		*str == 'l' || *str == 'R' || \
		*str == 'a' || *str == 'r' || \
		*str == 't' || *str == 'n' || \
		*str == 'G' || *str == 'A' || \
		ft_strcmp(str, "-linux-sort") == 0)
		return (1);
	return (0);
}
